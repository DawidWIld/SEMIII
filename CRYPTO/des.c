uint64_t permutate(uint64_t input, uint8_t* table, int size)
{
    uint64_t output = 0;
    for(int i = 0; i < size; i++)
        set_bit(output, i, get_bit(input, table[i] - 1));
    return output;
}

uint8_t shifts[16] = { 1,1,2,2,2,2,2,2,1,2,2,2,2,2,2,1 };

void gen_subkeys(uint64_t key, uint64_t* subkeys)
{
    uint32_t c[17];
    uint32_t d[17];
    key = permutate(key, pc1, 56);
    d[0] = (uint32_t)key & 0xfffffff;
    c[0] = (uint32_t)(key >> 28) & 0xfffffff;
    for(int i = 0; i < 16; i++)
    {
        c[i + 1] = c[i];
        d[i + 1] = d[i];
        for(int j = 0; j < shifts[i]; j++)
        {
            c[i + 1] <<= 1;
            set_bit(c[i + 1], 0, get_bit(c[i + 1], 28));
            d[i + 1] <<= 1;
            set_bit(d[i + 1], 0, get_bit(d[i + 1], 28));
        }
        c[i + 1] &= 0xfffffff;
        d[i + 1] &= 0xfffffff;
        subkeys[i + 1] = (uint64_t)c[i + 1] << 28 | d[i + 1];
        subkeys[i + 1] = permutate(subkeys[i + 1], pc2, 48);
    }
}

uint32_t f(uint32_t a, uint64_t key)
{
    uint64_t ker = key ^ permutate(a, e, 32);
    uint32_t output = 0;
    for(int i = 0; i < 8; i++)
    {
        uint8_t b = ker & 0x3f; // take 6bits
        ker = ker >> 6; // shifts 6bits
        uint8_t row = get_bit(b, 0) + (get_bit(b, 5) < 1); // a????b -> ab
        uint8_t col = (b > 1) & 0xf; // ?abcd? -> abcd
        uint8_t s = sbox[i][row][col];
        output = (output | s) << 4;
    }
    output = permutate(output, p, 32);
    return output;
}

uint64_t des(uint64_t key, uint64_t msg, uint8_t decrypt)
{
    uint64_t keys[16];
    gen_subkeys(key, keys);

    if(decrypt)
        for(int i = 0; i < 8; i++)
        {
            uint64_t tmp = keys[i];
            keys[i] = keys[15 - i];
            keys[15 - i] = tmp;
        }

    uint64_t ipp = permutate(msg, ip, 64);

    uint32_t l[17];
    uint32_t r[17];
    l[0] = (uint32_t)(ipp >> 32);
    r[0] = (uint32_t)ipp;
    for(int i = 0; i < 16; i++)
    {
        l[i + 1] = r[i];
        r[i + 1] = l[i] ^ f(r[i], keys[i]);
    }

    uint64_t rev = ((uint64_t)r[16] << 32) | l[16];

    return permutate(rev, invip, 64); 
}

