#define get_bit(v, n) ((v >> n) & 1UL)
#define set_bit(v, n, b) (v ^= (-b ^ v) & (1UL << n))

void printb(uint64_t binary, int size)
{
    for(int i = size-1; i >= 0; i--)
    {
        putchar('0' + ((binary >> i) & 1));
        if(i % 4 == 0) putchar(' ');
    }
    putchar('\n');
}

